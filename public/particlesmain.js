  function updateShootingStarPosition() {
    const shootingStarContainer = document.querySelector(".shooting-star-container");
    if (shootingStarContainer) {
      const scrollPercentage = horizontalScrollContainer.scrollLeft / (horizontalScrollContainer.scrollWidth - horizontalScrollContainer.clientWidth);
      shootingStarContainer.style.transform = `translateY(${-scrollPercentage * 200}vh)`;
      shootingStarContainer.style.opacity = Math.max(1 - scrollPercentage*5, 0);
    }
  }
  
  horizontalScrollContainer.addEventListener("scroll", updateShootingStarPosition);
  
  